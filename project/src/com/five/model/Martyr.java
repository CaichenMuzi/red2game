package com.five.model;

/**
 * 宠物信息
 * @author 
 *
 */
public class Martyr
{
    /**
     * 宠物昵称
     */
    private String name;
    
    /**
     * 宠物id
     */
    private String mid;
    
    /**
     * 宠物类型
     */
    private String type;
    
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public String getMid()
    {
        return mid;
    }
    public void setMid(String mid)
    {
        this.mid = mid;
    }
    public String getType()
    {
        return type;
    }
    public void setType(String type)
    {
        this.type = type;
    }
    
    
    
    
}
